    /** Event for the ok Button. Adds the stored names to the current division
     * and closes the window.
     * 
     * @param evt the action event for the button press
     */
    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) newTeamsTable.getModel();
        int rows = model.getRowCount();
        String[] teams = new String[rows];
        for (int i = 0; i < rows; ++i) {
            teams[i] = (String) model.getValueAt(i, 0);
        }
        manager.addTeams(teams);
        this.dispose();
    }//GEN-LAST:event_okButtonActionPerformed

