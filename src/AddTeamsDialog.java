    /** Event for the Add Button - Takes the string in the new team text box
     * and adds it to the list of team names
     * 
     * @param evt the action event for the button press
     */
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String newTeamName = this.newTeam.getText();
        if (newTeamName.equals("")) return;
        DefaultTableModel model = (DefaultTableModel) newTeamsTable.getModel();
        
        model.addRow(new String[]{newTeamName});
        
        newTeam.setText("");
    }//GEN-LAST:event_addButtonActionPerformed

